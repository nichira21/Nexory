    <script>
        const c = document.getElementById("canv");
        const ctx = c.getContext("2d");

        function resize() {
            c.width = window.innerWidth;
            c.height = window.innerHeight;
        }
        resize();
        window.addEventListener("resize", resize);

        const cx = () => c.width / 2;
        const cy = () => c.height / 2;

        let time = 0;

        const rand = (min, max) => Math.random() * (max - min) + min;

        const layers = Array.from({
            length: 5
        }, () => ({
            a: rand(40, 200),
            b: rand(80, 320),
            c: rand(120, 500),
            speed: rand(0.00008, 0.00018),
            alpha: rand(0.1, 0.3)
        }));

        function curve(t, l) {
            return {
                x: Math.sin(t * 1.1) * l.a +
                    Math.sin(t * 0.7) * l.b +
                    Math.cos(t * 0.3) * l.c,
                y: Math.cos(t * 1.0) * l.a +
                    Math.sin(t * 0.6) * l.b +
                    Math.sin(t * 0.2) * l.c
            };
        }

        function drawLayer(layer, offset) {
            ctx.beginPath();
            let t = time + offset;
            let p = curve(t, layer);

            ctx.moveTo(
                p.x + cx(),
                p.y + cy()
            );

            for (let i = 0; i < 420; i++) {
                p = curve(t, layer);
                ctx.lineTo(
                    p.x + cx(),
                    p.y + cy()
                );
                t += 0.02;
            }

            ctx.strokeStyle = `rgba(255,255,255,${layer.alpha})`;
            ctx.lineWidth = 0.6;
            ctx.stroke();
        }

        function draw() {
            ctx.clearRect(0, 0, c.width, c.height);

            time += 0.0502;

            // subtle global rotation
            ctx.save();
            ctx.translate(cx(), cy());
            // ctx.rotate(Math.sin(time * 0.4) * 0.02);
            ctx.translate(-cx(), -cy());

            layers.forEach((layer, i) => {
                drawLayer(layer, i * 20);
            });

            ctx.restore();

            requestAnimationFrame(draw);
        }

        draw();
    </script>


